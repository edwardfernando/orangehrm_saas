package efs.thesis.common.pojo;

import efs.thesis.saas.model.Employee;
import efs.thesis.saas.model.EmployeeAccess;

/**
 * @author Edward Fernando
 * @project Thesis Project, 2013
 */

public class UserContext {

	private Employee employee;
	private EmployeeAccess approvalAccess;
	
	public UserContext(){
		
	}
	public UserContext(Employee employee){
		this.employee = employee;
	}
	
	public Employee getEmployee() {
		return employee;
	}

	public void setEmployee(Employee employee) {
		this.employee = employee;
	}
	public EmployeeAccess getApprovalAccess() {
		return approvalAccess;
	}
	public void setApprovalAccess(EmployeeAccess approvalAccess) {
		this.approvalAccess = approvalAccess;
	}
	@Override
	public String toString() {
		return "UserContext [employee=" + employee + ", approvalAccess="
				+ approvalAccess + "]";
	}
	
	
}
